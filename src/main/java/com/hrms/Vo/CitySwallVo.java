public class CitySwallVo {

	//园区编号
	private String yardNumber;
	//产品类型
	private String productType;
	//当前月份
	private String months;
	//当前年份
	private String fullYears;

	public String getYardNumber() {
		return yardNumber;
	}

	public void setYardNumber(String yardNumber) {
		this.yardNumber = yardNumber;
	}

	public String getProductType() {
		return productType;
	}

	public void setProductType(String productType) {
		this.productType = productType;
	}

	public String getMonths() {
		return months;
	}

	public void setMonths(String months) {
		this.months = months;
	}

	public String getFullYears() {
		return fullYears;
	}

	public void setFullYears(String fullYears) {
		this.fullYears = fullYears;
	}


}
